#include "libft.h"

int	ft_strnequ(const char *s1, const char *s2, size_t n)
{
	size_t i;

	i = 0;
	if ((s1 && !s2) || (s2 && !s1))
		return (0);
	if (!s1 && !s2)
		return (1);
	while (n > 0 && (s1[n] || s2[n]))
	{
		if (s1[i] != s2[i])
			return (0);
		i++;
		n--;
	}
	return (1);
}
