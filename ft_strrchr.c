#include "./libft.h"

char *ft_strrchr(const char *s, int c)
{
	int i;
	char *ptr;

	i = 0;
	ptr = NULL;
	while (s[i] != '\0')
	{
		if (s[i] == c)
		{
			ptr = (char *)&c;
			i++;
		}
		i++;
	}
	return ((char *)ptr);
}
